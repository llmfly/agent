-- ============================================================================
-- 电商管理系统 - 测试数据库初始化脚本
-- 用于 Text-to-SQL 功能测试（商品/订单/用户/库存 等电商场景）
-- ============================================================================

CREATE DATABASE IF NOT EXISTS `ecommerce`
  DEFAULT CHARACTER SET utf8mb4
  DEFAULT COLLATE utf8mb4_unicode_ci;

USE `ecommerce`;

-- ============================================================================
-- 1. 用户表
-- ============================================================================
CREATE TABLE IF NOT EXISTS `users` (
  `id`          INT AUTO_INCREMENT COMMENT '用户ID',
  `name`        VARCHAR(50) NOT NULL COMMENT '用户名',
  `phone`       VARCHAR(20) DEFAULT NULL COMMENT '手机号',
  `email`       VARCHAR(100) DEFAULT NULL COMMENT '邮箱',
  `gender`      ENUM('男','女','未知') DEFAULT '未知' COMMENT '性别',
  `birthday`    DATE DEFAULT NULL COMMENT '生日',
  `city`        VARCHAR(50) DEFAULT NULL COMMENT '所在城市',
  `level`       ENUM('普通','白银','黄金','钻石') DEFAULT '普通' COMMENT '会员等级',
  `points`      INT DEFAULT 0 COMMENT '积分',
  `status`      ENUM('正常','冻结','注销') DEFAULT '正常' COMMENT '状态',
  `created_at`  DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '注册时间',
  PRIMARY KEY (`id`),
  KEY `idx_phone` (`phone`),
  KEY `idx_level` (`level`),
  KEY `idx_city` (`city`),
  KEY `idx_created` (`created_at`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='用户表';

-- ============================================================================
-- 2. 商品分类表
-- ============================================================================
CREATE TABLE IF NOT EXISTS `categories` (
  `id`          INT AUTO_INCREMENT COMMENT '分类ID',
  `name`        VARCHAR(50) NOT NULL COMMENT '分类名称',
  `parent_id`   INT DEFAULT NULL COMMENT '父分类ID',
  `sort_order`  INT DEFAULT 0 COMMENT '排序',
  `created_at`  DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  PRIMARY KEY (`id`),
  KEY `idx_parent` (`parent_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='商品分类表';

-- ============================================================================
-- 3. 商品表
-- ============================================================================
CREATE TABLE IF NOT EXISTS `products` (
  `id`            INT AUTO_INCREMENT COMMENT '商品ID',
  `name`          VARCHAR(200) NOT NULL COMMENT '商品名称',
  `category_id`   INT DEFAULT NULL COMMENT '所属分类',
  `brand`         VARCHAR(100) DEFAULT NULL COMMENT '品牌',
  `price`         DECIMAL(10,2) NOT NULL COMMENT '单价',
  `cost_price`    DECIMAL(10,2) DEFAULT NULL COMMENT '成本价',
  `stock`         INT DEFAULT 0 COMMENT '库存数量',
  `sales_count`   INT DEFAULT 0 COMMENT '销量',
  `rating`        DECIMAL(2,1) DEFAULT 0.0 COMMENT '评分(1.0-5.0)',
  `description`   TEXT DEFAULT NULL COMMENT '商品描述',
  `status`        ENUM('上架','下架','待审核') DEFAULT '上架' COMMENT '状态',
  `created_at`    DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updated_at`    DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (`id`),
  KEY `idx_category` (`category_id`),
  KEY `idx_brand` (`brand`),
  KEY `idx_price` (`price`),
  KEY `idx_status` (`status`),
  KEY `idx_sales` (`sales_count`),
  CONSTRAINT `fk_product_category` FOREIGN KEY (`category_id`) REFERENCES `categories`(`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='商品表';

-- ============================================================================
-- 4. 订单表
-- ============================================================================
CREATE TABLE IF NOT EXISTS `orders` (
  `id`            INT AUTO_INCREMENT COMMENT '订单ID',
  `order_no`      VARCHAR(30) NOT NULL COMMENT '订单号',
  `user_id`       INT NOT NULL COMMENT '用户ID',
  `total_amount`  DECIMAL(12,2) NOT NULL COMMENT '订单总金额',
  `discount`      DECIMAL(10,2) DEFAULT 0.00 COMMENT '优惠金额',
  `pay_amount`    DECIMAL(12,2) NOT NULL COMMENT '实付金额',
  `payment_method` ENUM('微信','支付宝','银行卡','余额') DEFAULT '微信' COMMENT '支付方式',
  `status`        ENUM('待付款','已付款','已发货','已完成','已取消','退款中','已退款') DEFAULT '待付款' COMMENT '订单状态',
  `address`       VARCHAR(200) DEFAULT NULL COMMENT '收货地址',
  `receiver`      VARCHAR(50) DEFAULT NULL COMMENT '收货人',
  `receiver_phone` VARCHAR(20) DEFAULT NULL COMMENT '收货人电话',
  `remark`        VARCHAR(500) DEFAULT NULL COMMENT '备注',
  `paid_at`       DATETIME DEFAULT NULL COMMENT '支付时间',
  `shipped_at`    DATETIME DEFAULT NULL COMMENT '发货时间',
  `created_at`    DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_order_no` (`order_no`),
  KEY `idx_user` (`user_id`),
  KEY `idx_status` (`status`),
  KEY `idx_created` (`created_at`),
  KEY `idx_paid` (`paid_at`),
  CONSTRAINT `fk_order_user` FOREIGN KEY (`user_id`) REFERENCES `users`(`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='订单表';

-- ============================================================================
-- 5. 订单明细表
-- ============================================================================
CREATE TABLE IF NOT EXISTS `order_items` (
  `id`            INT AUTO_INCREMENT COMMENT '明细ID',
  `order_id`      INT NOT NULL COMMENT '订单ID',
  `product_id`    INT NOT NULL COMMENT '商品ID',
  `product_name`  VARCHAR(200) NOT NULL COMMENT '商品名称（快照）',
  `price`         DECIMAL(10,2) NOT NULL COMMENT '购买单价',
  `quantity`      INT NOT NULL COMMENT '购买数量',
  `subtotal`      DECIMAL(12,2) NOT NULL COMMENT '小计金额',
  `created_at`    DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  PRIMARY KEY (`id`),
  KEY `idx_order` (`order_id`),
  KEY `idx_product` (`product_id`),
  CONSTRAINT `fk_item_order` FOREIGN KEY (`order_id`) REFERENCES `orders`(`id`) ON DELETE CASCADE,
  CONSTRAINT `fk_item_product` FOREIGN KEY (`product_id`) REFERENCES `products`(`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='订单明细表';

-- ============================================================================
-- 6. 购物车表
-- ============================================================================
CREATE TABLE IF NOT EXISTS `carts` (
  `id`          INT AUTO_INCREMENT COMMENT '购物车ID',
  `user_id`     INT NOT NULL COMMENT '用户ID',
  `product_id`  INT NOT NULL COMMENT '商品ID',
  `quantity`    INT DEFAULT 1 COMMENT '数量',
  `selected`    TINYINT(1) DEFAULT 1 COMMENT '是否选中',
  `created_at`  DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  `updated_at`  DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP COMMENT '更新时间',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_user_product` (`user_id`, `product_id`),
  KEY `idx_product` (`product_id`),
  CONSTRAINT `fk_cart_user` FOREIGN KEY (`user_id`) REFERENCES `users`(`id`),
  CONSTRAINT `fk_cart_product` FOREIGN KEY (`product_id`) REFERENCES `products`(`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='购物车表';

-- ============================================================================
-- 7. 商品评价表
-- ============================================================================
CREATE TABLE IF NOT EXISTS `reviews` (
  `id`            INT AUTO_INCREMENT COMMENT '评价ID',
  `product_id`    INT NOT NULL COMMENT '商品ID',
  `user_id`       INT NOT NULL COMMENT '用户ID',
  `order_id`      INT DEFAULT NULL COMMENT '关联订单ID',
  `rating`        TINYINT NOT NULL COMMENT '评分(1-5)',
  `content`       TEXT DEFAULT NULL COMMENT '评价内容',
  `has_image`     TINYINT(1) DEFAULT 0 COMMENT '是否有图',
  `created_at`    DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  PRIMARY KEY (`id`),
  KEY `idx_product` (`product_id`),
  KEY `idx_user` (`user_id`),
  CONSTRAINT `fk_review_product` FOREIGN KEY (`product_id`) REFERENCES `products`(`id`),
  CONSTRAINT `fk_review_user` FOREIGN KEY (`user_id`) REFERENCES `users`(`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='商品评价表';

-- ============================================================================
-- 8. 供应商表
-- ============================================================================
CREATE TABLE IF NOT EXISTS `suppliers` (
  `id`            INT AUTO_INCREMENT COMMENT '供应商ID',
  `name`          VARCHAR(100) NOT NULL COMMENT '供应商名称',
  `contact`       VARCHAR(50) DEFAULT NULL COMMENT '联系人',
  `phone`         VARCHAR(20) DEFAULT NULL COMMENT '联系电话',
  `address`       VARCHAR(200) DEFAULT NULL COMMENT '地址',
  `cooperation_status` ENUM('合作中','暂停','终止') DEFAULT '合作中' COMMENT '合作状态',
  `created_at`    DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='供应商表';

-- ============================================================================
-- 9. 采购入库表
-- ============================================================================
CREATE TABLE IF NOT EXISTS `purchases` (
  `id`            INT AUTO_INCREMENT COMMENT '采购ID',
  `supplier_id`   INT NOT NULL COMMENT '供应商ID',
  `product_id`    INT NOT NULL COMMENT '商品ID',
  `quantity`      INT NOT NULL COMMENT '采购数量',
  `unit_cost`     DECIMAL(10,2) NOT NULL COMMENT '采购单价',
  `total_cost`    DECIMAL(12,2) NOT NULL COMMENT '采购总金额',
  `purchased_at`  DATE DEFAULT NULL COMMENT '采购日期',
  `created_at`    DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  PRIMARY KEY (`id`),
  KEY `idx_supplier` (`supplier_id`),
  KEY `idx_product` (`product_id`),
  KEY `idx_purchased` (`purchased_at`),
  CONSTRAINT `fk_purchase_supplier` FOREIGN KEY (`supplier_id`) REFERENCES `suppliers`(`id`),
  CONSTRAINT `fk_purchase_product` FOREIGN KEY (`product_id`) REFERENCES `products`(`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='采购入库表';

