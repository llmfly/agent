-- ============================================================================
-- 学生管理系统 - 测试数据库初始化脚本
-- 目标: 创建一个完整的课程成绩管理系统用于 Text-to-SQL 功能测试
-- ============================================================================

-- 创建数据库
CREATE DATABASE IF NOT EXISTS `student`
  DEFAULT CHARACTER SET utf8mb4
  DEFAULT COLLATE utf8mb4_unicode_ci;

USE `student`;

-- ============================================================================
-- 1. 学生表
-- ============================================================================
CREATE TABLE IF NOT EXISTS `students` (
  `student_id`   VARCHAR(20)  NOT NULL COMMENT '学号',
  `name`         VARCHAR(50)  NOT NULL COMMENT '姓名',
  `gender`       ENUM('男','女') DEFAULT NULL COMMENT '性别',
  `birth_date`   DATE         DEFAULT NULL COMMENT '出生日期',
  `class_name`   VARCHAR(30)  DEFAULT NULL COMMENT '班级',
  `major`        VARCHAR(50)  DEFAULT NULL COMMENT '专业',
  `enroll_year`  INT          DEFAULT NULL COMMENT '入学年份',
  `phone`        VARCHAR(20)  DEFAULT NULL COMMENT '联系电话',
  `email`        VARCHAR(100) DEFAULT NULL COMMENT '电子邮箱',
  `status`       ENUM('在读','休学','毕业','退学') DEFAULT '在读' COMMENT '状态',
  `created_at`   DATETIME     DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  PRIMARY KEY (`student_id`),
  KEY `idx_class` (`class_name`),
  KEY `idx_major` (`major`),
  KEY `idx_enroll_year` (`enroll_year`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='学生信息表';

-- ============================================================================
-- 2. 教师表
-- ============================================================================
CREATE TABLE IF NOT EXISTS `teachers` (
  `teacher_id`  VARCHAR(20) NOT NULL COMMENT '教师编号',
  `name`        VARCHAR(50) NOT NULL COMMENT '教师姓名',
  `gender`      ENUM('男','女') DEFAULT NULL COMMENT '性别',
  `title`       VARCHAR(30) DEFAULT NULL COMMENT '职称 (教授/副教授/讲师/助教)',
  `department`  VARCHAR(50) DEFAULT NULL COMMENT '所属院系',
  `phone`       VARCHAR(20) DEFAULT NULL COMMENT '联系电话',
  `email`       VARCHAR(100) DEFAULT NULL COMMENT '电子邮箱',
  `hire_date`   DATE DEFAULT NULL COMMENT '入职日期',
  `created_at`  DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  PRIMARY KEY (`teacher_id`),
  KEY `idx_department` (`department`),
  KEY `idx_title` (`title`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='教师信息表';

-- ============================================================================
-- 3. 课程表
-- ============================================================================
CREATE TABLE IF NOT EXISTS `courses` (
  `course_id`     VARCHAR(20) NOT NULL COMMENT '课程编号',
  `name`          VARCHAR(100) NOT NULL COMMENT '课程名称',
  `credits`       DECIMAL(3,1) DEFAULT NULL COMMENT '学分',
  `hours`         INT DEFAULT NULL COMMENT '课时',
  `course_type`   ENUM('必修','选修','公共课','实践') DEFAULT '必修' COMMENT '课程类型',
  `teacher_id`    VARCHAR(20) DEFAULT NULL COMMENT '授课教师编号',
  `department`    VARCHAR(50) DEFAULT NULL COMMENT '开课院系',
  `max_students`  INT DEFAULT 60 COMMENT '最大选课人数',
  `semester`      VARCHAR(20) DEFAULT NULL COMMENT '开课学期 (如 2025-2026-1)',
  `description`   TEXT DEFAULT NULL COMMENT '课程简介',
  `created_at`    DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  PRIMARY KEY (`course_id`),
  KEY `idx_teacher` (`teacher_id`),
  KEY `idx_semester` (`semester`),
  KEY `idx_course_type` (`course_type`),
  CONSTRAINT `fk_course_teacher` FOREIGN KEY (`teacher_id`) REFERENCES `teachers`(`teacher_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='课程信息表';

-- ============================================================================
-- 4. 选课表 (成绩表)
-- ============================================================================
CREATE TABLE IF NOT EXISTS `enrollments` (
  `id`            INT AUTO_INCREMENT COMMENT '自增主键',
  `student_id`    VARCHAR(20) NOT NULL COMMENT '学号',
  `course_id`     VARCHAR(20) NOT NULL COMMENT '课程编号',
  `semester`      VARCHAR(20) DEFAULT NULL COMMENT '学期',
  `score`         DECIMAL(5,1) DEFAULT NULL COMMENT '成绩 (0-100)',
  `grade_point`   DECIMAL(3,1) DEFAULT NULL COMMENT '绩点',
  `grade`         ENUM('优秀','良好','中等','及格','不及格') DEFAULT NULL COMMENT '等级',
  `is_retake`     TINYINT(1) DEFAULT 0 COMMENT '是否重修 (0:否, 1:是)',
  `enrolled_at`   DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '选课时间',
  PRIMARY KEY (`id`),
  UNIQUE KEY `uk_stu_course_sem` (`student_id`, `course_id`, `semester`),
  KEY `idx_student` (`student_id`),
  KEY `idx_course` (`course_id`),
  KEY `idx_score` (`score`),
  KEY `idx_semester_grade` (`semester`, `grade`),
  CONSTRAINT `fk_enroll_student` FOREIGN KEY (`student_id`) REFERENCES `students`(`student_id`),
  CONSTRAINT `fk_enroll_course` FOREIGN KEY (`course_id`) REFERENCES `courses`(`course_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='选课成绩表';

-- ============================================================================
-- 5. 院系表
-- ============================================================================
CREATE TABLE IF NOT EXISTS `departments` (
  `dept_id`    VARCHAR(10) NOT NULL COMMENT '院系编号',
  `name`       VARCHAR(50) NOT NULL COMMENT '院系名称',
  `dean`       VARCHAR(50) DEFAULT NULL COMMENT '院长/系主任',
  `phone`      VARCHAR(20) DEFAULT NULL COMMENT '联系电话',
  `location`   VARCHAR(100) DEFAULT NULL COMMENT '办公地点',
  `created_at` DATETIME DEFAULT CURRENT_TIMESTAMP COMMENT '创建时间',
  PRIMARY KEY (`dept_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COMMENT='院系信息表';

